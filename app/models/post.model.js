class Post {
  constructor(id, author, title, body, created, updated, published, comments, tags, statistics) 
  {
    this.id = id,
    this.author = author,
    this.title = title,
    this.body = body,
    this.created = created;
    this.updated = updated;
    this.published = published;
    this.comments = comments;
    this.tags = tags;
    this.statistics = statistics;
  }
}